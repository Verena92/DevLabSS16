package de.hdm.wim.token;

public class Author {

	private String employeeId;
	private String firstName;
	private String lastName;
	private String worksFor;
	private String partOfProject;

	public String getEmployeeId() {
		return employeeId;
	}

	public void setEmployeeId(String employeeId) {
		this.employeeId = employeeId;
	}

	public String getFirstName() {
		return firstName;
	}

	public void setFirstName(String firstName) {
		this.firstName = firstName;
	}

	public String getLastName() {
		return lastName;
	}

	public void setLastName(String lastName) {
		this.lastName = lastName;
	}

	public String getWorksFor() {
		return worksFor;
	}

	public void setWorksFor(String worksFor) {
		this.worksFor = worksFor;
	}

	public String getPartOfProject() {
		return partOfProject;
	}

	public void setPartOfProject(String partOfProject) {
		this.partOfProject = partOfProject;
	}

}
